    private void loadDataTable() {
        tableModel = new DefaultTableModel();
        try {
            String cariData = txtCari.getText();
            List<Account> listAccount = accountCtr.getData(cariData);
            Object[] colums = {"Account ID", "Name", "Type", "Aksi"};
            tableModel.setColumnIdentifiers(colums);
            
            for (Account account : listAccount) {
                tableModel.addRow(new Object[]{
                    account.getAccountId(), 
                    account.getName(), 
                    account.getType(), 
                    "", // tambahkan 1 value kosong untuk kolom button action (edit dan delete)
                });
            }
            
            customTable1.setModel(tableModel);
            customTable1.setShowActionButtons(true);

            customTable1.setActionButtonListener(new CustomTable.ActionButtonListener() {
                @Override
                public void onEdit(int row, Object[] rowData) {
                    String accountId = rowData[0].toString();
                    showDataToForm(accountId);
                }

                @Override
                public void onDelete(int row, Object[] rowData) {
                    int result = JOptionPane.showConfirmDialog(null, 
                        "Delete Account " + rowData[2] + "?", 
                        "Confirm", 
                        JOptionPane.YES_NO_OPTION);
                    if (result == JOptionPane.YES_OPTION) {
                        customTable1.removeRow(row);
                    }
                }
            });
        
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(null, "data account gagal dipanggil "+e);
        
        } 
    }
    
    private void showDataToForm(String accountId) {
        try {
            Account account = accountCtr.getAccountById(accountId);
            
            lblTitleForm.setText("Form Edit Data Akun");
            
            btnSimpan.setText("Update");
            txtAccountId.setText(account.getAccountId());
            txtName.setText(account.getName());
            cbType.setSelectedItem(account.getType());
            
            txtAccountId.setEnabled(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "data user gagal dipanggil "+e);
        }
    }
    
    private void save() {
        Account account = new Account();
        account.setAccountId(txtAccountId.getText());
        account.setName(txtName.getText());
        account.setType(cbType.getSelectedItem().toString());
        
        boolean isCreateAccount = btnSimpan.getText().equals("Tambah");
        
        if (isCreateAccount) {
            String create = accountCtr.createAccount(account);
            JOptionPane.showMessageDialog(null, create);
        } else {
            String update = accountCtr.updateAccount(account);
            JOptionPane.showMessageDialog(null, update);
        }
        loadDataTable();
       
    }
    
    private void resetForm() {
        txtAccountId.setText("");
        txtAccountId.setEnabled(true);
        txtName.setText("");
        cbType.setSelectedIndex(0);
        txtCari.setText("");
        btnSimpan.setText("Tambah");
        lblTitleForm.setText("Form Tambah Data Akun");
    }
    

